def build_regression_vs_ranking_comparison(metrics: Dict[str, pd.DataFrame]) -> str:
    """Compare regression models (v1,v2,v4a,v4b) vs ranking models (v3,v4c,v5)."""
    rows = []
    
    # Regression models comparison
    regression_models = ["v1", "v2", "v4a", "v4b"]
    for model in regression_models:
        if model not in metrics or metrics[model].empty:
            continue
        df = metrics[model]
        rows.append({
            "Paradigm": "Regression",
            "Model": model.upper(),
            "Metric": "Mean R²",
            "Value": f"{df.get('r2', pd.Series([0])).mean():+.3f}",
            "Status": "FAILED" if df.get("r2", pd.Series([0])).mean() < -0.1 else "PARTIAL",
        })
    
    # Ranking models comparison
    ranking_models = ["v3", "v4c", "v5"]
    for model in ranking_models:
        if model not in metrics or metrics[model].empty:
            continue
        df = metrics[model]
        mean_sharpe = df.get("sharpe_ann", pd.Series([0])).mean()
        status = "SUCCESS ✓" if mean_sharpe > 0.15 else ("IMPROVED" if mean_sharpe > 0 else "FAILED")
        rows.append({
            "Paradigm": "Ranking (L-S)",
            "Model": model.upper(),
            "Metric": "Mean Sharpe",
            "Value": f"{mean_sharpe:+.3f}",
            "Status": status,
        })
    
    if not rows:
        return "<p><em>No comparison data available.</em></p>"
    
    df_table = pd.DataFrame(rows)
    return df_table.to_html(classes="metrics-table", index=False, border=0, escape=False)


