            mean_r2 = df.get("r2", pd.Series([0])).mean()
            mean_edge = df.get("edge", pd.Series([0])).mean()
            arch = "Regression"
            metric_col = "Mean R²"
            metric_val = f"{mean_r2:+.3f}"
