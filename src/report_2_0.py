            h3 = f"{float(df[df['horizon']==3]['r2'].iloc[0]):+.3f}" if "r2" in df.columns and len(df[df["horizon"]==3]) > 0 else "—"
            h6 = f"{float(df[df['horizon']==6]['r2'].iloc[0]):+.3f}" if "r2" in df.columns and len(df[df["horizon"]==6]) > 0 else "—"
